#include "ft_printf.h"

int	putstr_c(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		if (write(1, "(null)", 6) < 0)
			return (-1);
		return (6);
	}
	while (*s)
	{
		if (write(1, s, 1) < 0)
			return (-1);
		s++;
		i++;
	}
	return (i);
}
